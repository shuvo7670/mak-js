function ali_hoss(){
    console.log('I am ali hossain');
}
ali_hoss();

// Function Expression (Anonymous Function)
const ali_age = function(age){
    console.log(`I am ${age} years old`);
};
ali_age(40);

// Arrow function 
const print_name = (name) => {
    console.log(`My name is ${name}`);
};
print_name('Ali Hossain');